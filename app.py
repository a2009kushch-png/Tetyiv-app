import streamlit as st

st.set_page_config(page_title="Цифровий Тетіїв", page_icon="🏛️")

st.title("🏛️ Тетіїв у смартфоні")
st.subheader("Офіційний сервіс громади")

# Оновлене меню
menu = ["🏠 Головна", "📰 Новини міста", "🚌 Розклад автобусів", "📋 Документи", "🏥 Запис до лікаря"]
choice = st.sidebar.selectbox("Оберіть розділ:", menu)

if choice == "🏠 Головна":
    st.write("Ласкаво просимо! Оберіть послугу в меню зліва.")
    st.info("📌 ЦНАП працює: Пн-Пт 08:00 - 17:00")

elif choice == "📰 Новини міста":
    st.header("📢 Останні новини громади")
    st.write("- **Хлорування води:** КП «Тетіївводоканал» проводить планову дезінфекцію мереж.")
    st.write("- **Засідання ради:** Обговорено план благоустрою на весняний період.")
    st.write("- **Спорт:** Тетіївські спортсмени здобули перемогу на обласних змаганнях.")

elif choice == "🚌 Розклад автобусів":
    st.header("🚍 Міжміське сполучення")
    tab1, tab2 = st.tabs(["На Київ", "На Білу Церкву"])
    with tab1:
        st.table({"Маршрут": ["Тетіїв - Київ", "Тетіїв - Київ (АС Південна)"], "Час": ["06:20, 15:30", "10:40"]})
    with tab2:
        st.table({"Маршрут": ["Тетіїв - Біла Церква"], "Час": ["07:45, 12:10, 16:00"]})
    st.warning("⚠️ Розклад може змінюватися. Телефон автостанції: +38 (04560) 5-13-43")

elif choice == "📋 Документи":
    st.header("Замовлення довідок")
    name = st.text_input("Ваше ПІБ")
    doc = st.selectbox("Тип документа", ["Про склад сім'ї", "Про реєстрацію"])
    if st.button("Замовити"):
        st.success(f"Запит для {name} прийнято!")

elif choice == "🏥 Запис до лікаря":
    st.header("Електронна реєстратура")
    st.selectbox("Оберіть лікаря", ["Терапевт", "Стоматолог", "Педіатр"])
    st.date_input("Дата візиту")
    if st.button("Записатись"):
        st.balloons()
        st.success("Ви записані!")
        
      
